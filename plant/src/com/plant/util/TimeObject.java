package com.plant.util;

public class TimeObject {

  private String year;

  private String month;

  public TimeObject() {
  }

  public void setYear(String year) {
    this.year = year;
  }

  public String getYear() {
    return year;
  }

  public void setMonth(String month) {
    this.month = month;
  }

  public String getMonth() {
    return month;
  }
}
